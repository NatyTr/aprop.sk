function aprop_update_product_card_acf_text_field( $post_id, $meta_key, $field_key, $value ) {
    update_post_meta( $post_id, $meta_key, $value );
    update_post_meta( $post_id, '_' . $meta_key, $field_key );
}

function aprop_apply_product_card_migration() {
    $payload = aprop_product_card_migration_payload();
    $version = isset( $payload['version'] ) ? (string) $payload['version'] : '';
    $products = isset( $payload['products'] ) && is_array( $payload['products'] ) ? $payload['products'] : array();

    if ( $version === '' || empty( $products ) ) {
        return;
    }

    if ( get_option( 'aprop_product_card_migration_version' ) === $version ) {
        return;
    }

    $field_keys = array(
        'badge' => 'field_aprop_card_badge',
        'spec_1_label' => 'field_aprop_card_spec_1_label',
        'spec_1_value' => 'field_aprop_card_spec_1_value',
        'spec_2_label' => 'field_aprop_card_spec_2_label',
        'spec_2_value' => 'field_aprop_card_spec_2_value',
        'spec_3_label' => 'field_aprop_card_spec_3_label',
        'spec_3_value' => 'field_aprop_card_spec_3_value',
    );

    $updated_count = 0;

    foreach ( $products as $product_slug => $product_fields ) {
        $product = get_page_by_path( $product_slug, OBJECT, 'product' );

        if ( ! $product instanceof WP_Post ) {
            continue;
        }

        foreach ( $field_keys as $field_name => $field_key ) {
            if ( ! array_key_exists( $field_name, $product_fields ) ) {
                continue;
            }

            $value = is_string( $product_fields[ $field_name ] ) ? trim( $product_fields[ $field_name ] ) : '';
            $meta_key = $field_name === 'badge' ? 'aprop_card_badge' : 'aprop_card_' . $field_name;

            aprop_update_product_card_acf_text_field( $product->ID, $meta_key, $field_key, $value );
        }

        $updated_count++;
    }

    if ( $updated_count > 0 ) {
        update_option( 'aprop_product_card_migration_version', $version, false );
    }
}
add_action( 'init', 'aprop_apply_product_card_migration', 20 );
